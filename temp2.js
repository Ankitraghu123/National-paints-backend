function convertData(dataArray) {
    return dataArray.map((entry, index) => {
        const [id, date, checkIn, checkOut] = entry.split(',');
        return {
            id: index + 124, // Assuming you want to start from 124
            emp_id: parseInt(id.trim()),
            date: date.trim().split('-').reverse().join('-'), // Convert to YYYY-MM-DD format
            check_in: checkIn.trim(),
            check_out: checkOut.trim(),
        };
    });
}

